x_coords, y_coords = (float(input()) for _ in range(2))

if x_coords == 0 == y_coords:
    print("It's the origin!")
elif x_coords == 0 or y_coords == 0:
    print("One of the coordinates is equal to zero!")
else:
    if x_coords > 0 < y_coords:
        print("I")
    elif x_coords < 0 < y_coords:
        print("II")
    elif x_coords < 0 > y_coords:
        print("III")
    elif x_coords > 0 > y_coords:
        print("IV")
